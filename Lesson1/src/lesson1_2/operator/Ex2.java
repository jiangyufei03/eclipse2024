package lesson1_2.operator;

public class Ex2 {

	public static void main(String[] args) {
		//比較演算子
		//変数の初期化
		int a = 10;
		int b = 20;
		
		//aがbより小さい場合
		System.out.println(a < b);  //true
		
		//aがbより大きい場合
		System.out.println(a > b);  //false
		
		//aとｂが等しい(==)
		System.out.println(a == b);
		
		//aとbが等しくない（!=）
		System.out.println(a != b);
		
		
		//boolean a = true;
		//boolean b = true;
		//System.out.println(a <= b);
		
		//演算子は数値しか使えない
	
	}

}

package lesson1_2.operator;

public class Ex5 {

	public static void main(String[] args) {
		// 代入演算子
		//変数の初期化
		
		int a = 10;
		int total1 = 0;
		total1 = a + total1;
		
		System.out.println(total1);
		
		int b = 10;
		int total2 = 0;
		total2 += b; // total2 = total2 + b;
		System.out.println(total2);
		
		int c = 10;
		int total3 = 20;
		total3 -= c;
		System.out.println(total3);
		
		
		
		
	}

}

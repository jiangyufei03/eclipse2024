package lesson1_2.variable;

public class Ex1 {

	public static void main(String[] args) {
		//変数の初期か（宣言と代入を同時に行う）
		int age = 30;
		//中身を表示
		System.out.println(age);
		
		//変数中身を上書き
		age = 100;
		System.out.println(age);
		
	
		//*練習問題
		//変数名「height」値「160.5」
		// 変数名height を　コンソールに出力する
		
		double height = 160.5;
		System.out.println(height);
		

	}

}
